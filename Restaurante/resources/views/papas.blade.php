    <p>Las papas fritas, también conocidas como papas a la francesa, son un plato de patatas que se cocinan mediante fritura hasta que queden doradas y crujientes. <br>
      Pueden consumirse solas o condimentadas con sal, kétchup, mayonesa u otras salsas     
    </p>

     <h2 style="text-align:center;">Preparación</h2>
     <h3 style="text-align:center;">los pasos para preparar unas ricas papas fritas son los siguientes:</h3>
    <div class="preparacion" style="display:flex; flex-direction:column; align-items:center;">
        <ul>
            <li>Pelar las papas y córtalas en tiras finas</li>
            <li> Calentar el aceite en una sartén grande sobre fuego medio-alto. </li>
            <li>agregar una parte de las papas y cocinar, moviendo de vez en cuando, hasta que las papitas estén doradas</li>
            <li>Prepar la salsa con la que combinar las papas</li>
            <li>Retírar las papas del aceite y ponerlas en un plato cubierto con varias capas de servilletas de papel para que escurra el aceite.</li>
            <li>Sirvimos las papitas fritas con las salsas.</li>
         </ul>

