     <h2 style="text-align:center;">Preparación</h2>
     <h3 style="text-align:center;">los pasos para preparar las ricas pupusas son los siguientes:</h3>
    <div class="preparacion" style="display:flex; flex-direction:column; align-items:center;">
        <ul>
            <li>Mezclar la harina de maíz o arroz , con agua hasta formar la masa.</li>
            <li>Preparar el quesillo o queso mozarella hasta que no queden grumos.</li>
            <li>Hacer una tortilla, colocar en el centro el quesillo  junto con los demas ingredientes, palmearlo hasta formar una tortilla mediana.</li>
            <li>Colocarlas en una plancha cocinar hasta que este cocida y salga el queso de la tortilla.</li>
            <li>Servir junto con el curtido y las salsas.</li>
         </ul>

