     <h2 style="text-align:center;">Preparación</h2>
     <h3 style="text-align:center;">los pasos para preparar la pizza son los siguientes:</h3>
    <div class="preparacion" style="display:flex; flex-direction:column; align-items:center;">
        <ul>
            <li>Prepara la salsa de tomate en un procesador de alimentos ó en una licuadora.</li>
            <li>Ralla el queso mozzarella.</li>
            <li>Picar todos los demás ingredientes: el jamón, la cebolla y el pimentón en tiras, y si usas pepperoni ó salchichas, en rodajas muy finas</li>
            <li>Agregar la salsa de tomate sobre tu masa de pizza previamente preparada y lista en su respectivo molde. </li>
            <li>A continuación ponle el queso mozzarella rallado y luego los demás ingredientes.</li>
            <li>Llevar al horno, previamente precalentado a 200 grados centígrados.</li>
            <li>Deja cocinar por 10-15 minutos. </li>
            <li>Una vez lista tu pizza, córtala en su clásica forma triangular y disfrútala en familia.</li>
         </ul>

